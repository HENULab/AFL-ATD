def read_custom_dataset(dataset, idx, is_train):
    mean, std = CALCULATED_MEAN, CALCULATED_STD
    data_transforms = {
        'train': transforms.Compose([
            transforms.RandomHorizontalFlip(),
            transforms.RandomCrop(32, padding=4),
            transforms.ToTensor(),
            transforms.Normalize(mean, std)
        ]),
        'test': transforms.Compose([
            transforms.ToTensor(),
            transforms.Normalize(mean, std)
        ])
    }

    if is_train:
        data_dir = f"{DATASET_BASE_PATH}/{idx}/train"
        data_transform = data_transforms['train']
    else:
        data_dir = f"{DATASET_BASE_PATH}/{idx}/test"
        data_transform = data_transforms['test']

    image_datasets = datasets.ImageFolder(data_dir, data_transform)
    return image_datasets
